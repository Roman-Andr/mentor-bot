import { describe, it, expect, beforeAll } from "vitest";
import { readFileSync } from "fs";
import { resolve } from "path";

function flattenKeys(obj: Record<string, unknown>, prefix = ""): Set<string> {
  const keys = new Set<string>();
  for (const [k, v] of Object.entries(obj)) {
    const full = prefix ? `${prefix}.${k}` : k;
    if (v !== null && typeof v === "object" && !Array.isArray(v)) {
      for (const nested of flattenKeys(v as Record<string, unknown>, full)) {
        keys.add(nested);
      }
    } else {
      keys.add(full);
    }
  }
  return keys;
}

const messagesDir = resolve(__dirname, "../../messages");

function loadMessages(locale: string): Set<string> {
  const raw = readFileSync(resolve(messagesDir, `${locale}.json`), "utf8");
  return flattenKeys(JSON.parse(raw));
}

