describe("i18n key coverage", () => {
  let enKeys: Set<string>;
  let ruKeys: Set<string>;

  beforeAll(() => {
    enKeys = loadMessages("en");
    ruKeys = loadMessages("ru");
  });

  describe("en.json contains every key used in source code", () => {
    for (const key of USED_KEYS) {
      it(`en: ${key}`, () => {
        expect(enKeys.has(key), `Missing key "${key}" in en.json`).toBe(true);
      });
    }
  });

  describe("ru.json contains every key used in source code", () => {
    for (const key of USED_KEYS) {
      it(`ru: ${key}`, () => {
        expect(ruKeys.has(key), `Missing key "${key}" in ru.json`).toBe(true);
      });
    }
  });

  it("both locales have the same set of top-level namespaces", () => {
    const enRaw = JSON.parse(readFileSync(resolve(messagesDir, "en.json"), "utf8"));
    const ruRaw = JSON.parse(readFileSync(resolve(messagesDir, "ru.json"), "utf8"));
    expect(Object.keys(enRaw).sort()).toEqual(Object.keys(ruRaw).sort());
  });

  it("en.json and ru.json have the same total number of keys", () => {
    const enCount = enKeys.size;
    const ruCount = ruKeys.size;
    if (enCount !== ruCount) {
      const onlyInEn = [...enKeys].filter((k) => !ruKeys.has(k));
      const onlyInRu = [...ruKeys].filter((k) => !enKeys.has(k));
      const lines: string[] = [`Key count mismatch: en=${enCount}, ru=${ruCount}`];
      if (onlyInEn.length)
        lines.push(`Only in en.json (${onlyInEn.length}): ${onlyInEn.join(", ")}`);
      if (onlyInRu.length)
        lines.push(`Only in ru.json (${onlyInRu.length}): ${onlyInRu.join(", ")}`);
      throw new Error(lines.join("\n"));
    }
    expect(enCount).toBe(ruCount);
  });

  it("en.json and ru.json have identical key sets", () => {
    const onlyInEn = [...enKeys].filter((k) => !ruKeys.has(k)).sort();
    const onlyInRu = [...ruKeys].filter((k) => !enKeys.has(k)).sort();
    expect(onlyInEn, `Keys present in en.json but missing in ru.json`).toEqual([]);
    expect(onlyInRu, `Keys present in ru.json but missing in en.json`).toEqual([]);
  });
});
